package kr.co.koscom.oppf.spt.usr.mbrReg.service;

import java.io.Serializable;

import kr.co.koscom.oppf.cmm.service.ComDefaultListVO;
import lombok.Data;

/**
 * NiceReq
 * <p>
 * Created by P.K Choi on 2017-05-11.
 */

@SuppressWarnings("serial")
public class NiceReqVO  extends ComDefaultListVO{

	// 생년월일(8자리)
    private String birthDay;

	// 내국인 / 외국인 ( Y / N )
    private String nationality;

	// 성별 ( M / F )
    private String sex;
    
    // 성명
    private String sname;
    
    // 이통사 구분 (SKT : 1 / KT : 2 / LG : 3 / SKT알뜰폰 : 5 / KT알뜰폰 : 6 / LGU+알뜰폰 : 7)
    private String smobileCo;
    
    // 휴대폰 번호
    private String smobileNo;
    
    // 응답 고유번호 (CPMobileStep1 에서 확인된 cpMobile.getResponseSEQ() 데이타)
    private String sresSeq;
    
    // SMS 인증번호
    private String sauthNo;
	
	// 요청 고유번호 (CPMobileStep1 에서 정의한 cpMobile.getRequestSEQ() 데이타)
    private String scpRequest;

	public String getBirthDay() {
		return birthDay;
	}

	public void setBirthDay(String birthDay) {
		this.birthDay = birthDay;
	}

	public String getNationality() {
		return nationality;
	}

	public void setNationality(String nationality) {
		this.nationality = nationality;
	}

	public String getSex() {
		return sex;
	}

	public void setSex(String sex) {
		this.sex = sex;
	}

	public String getSname() {
		return sname;
	}

	public void setSname(String sname) {
		this.sname = sname;
	}

	public String getSmobileCo() {
		return smobileCo;
	}

	public void setSmobileCo(String smobileCo) {
		this.smobileCo = smobileCo;
	}

	public String getSmobileNo() {
		return smobileNo;
	}

	public void setSmobileNo(String smobileNo) {
		this.smobileNo = smobileNo;
	}

	public String getSauthNo() {
		return sauthNo;
	}

	public void setSauthNo(String sauthNo) {
		this.sauthNo = sauthNo;
	}

	public String getScpRequest() {
		return scpRequest;
	}

	public void setScpRequest(String scpRequest) {
		this.scpRequest = scpRequest;
	}

	public String getSresSeq() {
		return sresSeq;
	}

	public void setSresSeq(String sresSeq) {
		this.sresSeq = sresSeq;
	}

	
}
